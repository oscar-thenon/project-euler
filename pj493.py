def coeff(r):
    c = 1
    for i in range(len(r)):
        c *= (7-i)
      
    sr = set(r)
    
    for s in sr:
        c /= factorial(r.count(s))
    
    return c

def numcombinations(r):
    """ Calcule le nombre de configurations possibles où r est un
    tuplet où est stocké le nombre de répétitions. """
    d = len(r)
    N = 1
    N *= coeff(r)
    N *= f2050
    for k in r:
        N /= factorial(k)
        N /= factorial(10-k)
    for l in range(7-d):
        N /= f10
    return N

